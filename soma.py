# Programa para mostar o resultado da soma, multiplicacao, subtracao e divisao dos números 12 e 14
numero1 = 12 
numero2 = 14
adic = numero1 + numero2
mult = numero1 * numero2
subt = numero1 - numero2
divi = numero1 / numero2
print(f"O número {numero1} somado ao número {numero2} é: {adic}") 
print(f"A multiplicação de {numero1} * {numero2} é: {mult}")
print(f"A subtração de {numero1} - {numero2} é: {subt}")
print(f"A divisão de {numero1} / {numero2} é: {divi}")

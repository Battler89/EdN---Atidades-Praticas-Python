# Programa para calcular e exibir a multiplicacao do primeiro e segundo numeros menos a multiplicacao do terceiro e quatro numeros
a = int(input("Digite um número: "))
b = int(input("Digite um número: "))
c = int(input("Digite um número: "))
d = int(input("Digite um número: "))
diferenca = (a * b) - (c * d)
print(f"Diferenca: {diferenca}")
